public class Maths {

	public static void main(String[] args) {
		int i = 73;
		int k = 27;
		System.out.println(Math.max(i, k));
		//System.out.println(Math.max(56, 89));
		System.out.println(Math.min(i, k));
		System.out.println(Math.sqrt(81));
		System.out.println(Math.abs(-78));
		System.out.println(Math.abs(96));
		System.out.println(Math.random());
		System.out.println(Math.random()*10000);
	}

}
