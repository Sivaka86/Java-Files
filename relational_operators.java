class RelationalOperators{
public static void main(String arg[])
    {
        int a=28;
        int b=12;
        System.out.println("Here  a == b = " + (a == b) ); //Here  a == b = false  
        System.out.println("Here  a != b = " + (a != b) ); //Here  a != b = true
        System.out.println("Here  a > b = " + (a > b) ); //Here  a > b = true
        System.out.println("Here  a < b = " + (a < b) ); //Here  a < b = false
        System.out.println("Here  b >= a = " + (b >= a) ); //Here  b >= a = false
        System.out.println("Here  b <= a = " + (b <= a) ); //Here  b <= a = true    
    }
}


